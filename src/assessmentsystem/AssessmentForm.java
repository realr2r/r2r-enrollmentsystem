private void ListOfSubjects(){
        SubjectListValues();
    
        label_FullName.setText(infos.lastName+", "+infos.firstName+" "+infos.middleInitial);
        label_Course.setText(infos.course);
        label_YearLevelSem.setText(infos.yearLevel+" "+infos.semester);
        
        //Add user's subjects and units into a list
        DefaultListModel dm = new DefaultListModel();
        for(int i = 0; i<subjects.subjectList.length;i++)
            {
                dm.addElement(subjects.subjectList[i]+" - ("+subjects.subjectUnits[i]+") Unit[s]");
            }
        list_SubjectList.setModel(dm);
}
