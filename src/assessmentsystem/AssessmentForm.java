    private void SubjectEnlistmentAndAssessmentOfFees(){
    int units=0,labFee=0, tuitionFee =0, guidanceFee=100, insuranceFee=67, rso=50, studentActivitiesFee=100, studentNurturanceFee=364,technologyFee = 300, totalFees;
        
        DefaultListModel dm = new DefaultListModel();
        int[] subjectEnlisted = list_SubjectList.getSelectedIndices();
         
        //Get the user's selected subjects and it's units
        for (int i = 0; i < subjectEnlisted.length; i++) {
            dm.addElement(subjects.subjectList[subjectEnlisted[i]]+" - ("+subjects.subjectUnits[subjectEnlisted[i]]+") Unit[s]");
            units+=subjects.subjectUnits[subjectEnlisted[i]];
            if(subjects.subjectList[subjectEnlisted[i]].contains("LAB")){
                labFee+=128;
            }
         }
        jLabel1.setText("Total units: "+String.valueOf(units));
       list_SubjectEnlist.setModel(dm);
       
       DefaultListModel dm2 = new DefaultListModel();
       tuitionFee = units*524;
       totalFees = labFee+tuitionFee+guidanceFee+insuranceFee+rso+studentActivitiesFee+studentNurturanceFee+technologyFee;
       
       dm2.addElement("Tuition Fee: "+tuitionFee);
       dm2.addElement("Laboratory Fee: "+labFee);
       dm2.addElement("Guidance Fee: "+guidanceFee);
       dm2.addElement("Insurance Fee: "+insuranceFee);
       dm2.addElement("RSO: "+rso);
       dm2.addElement("Student Activities Fee: "+studentActivitiesFee);
       dm2.addElement("Student Nurturance Fee: "+studentNurturanceFee);
       dm2.addElement("Technology Fee: "+technologyFee);
       dm2.addElement("-----------------------------------------");
       dm2.addElement("Total Assessment: "+totalFees);
       list_AssessmentOfFees.setModel(dm2);
    }
